fun main() {
    val i1 = 12
    val i2 = 34

    // Operations on numbers
    println(i1 + i2)
    println(i1 - i2)
    println(i1 * i2)
    println(i1 / i2)

    // Doubles
    val d1 = 1.4
    val d2 = 2.5

    println(d1 + d2)
    println(d1 - d2)
    println(d1 * d2)
    println(d1 / d2)

    println()

    println(5.toDouble() / 2)

    println()

    // Remainder operator {%}
    println(1 % 3)
    println(2 % 3)
    println(3 % 3)
    println(4 % 3)
    println(5 % 3)
    println(6 % 3)
    println(7 % 3)
    println(0 % 3)
    println(-1 % 3)
    println(-2 % 3)
    println(-3 % 3)

    println()

    println(0b011 and 0b001)
    println(0b011 or 0b001)
    println(0b011 xor 0b001)
}